            <p class="med">The SRI International Network and Resources Center (SRI-Rice) was established at Cornell University in 2010 in response to the increasing importance of the System of Rice Intensification (SRI), a climate-smart, yield-increasing methodology that is being utilized by more than 10 million smallholder farmers in over 55 countries (see map). SRI-Rice is housed at International Programs of Cornell's College of Agriculture and Life Sciences.</p>

            <p class="med">The use of the SRI methodology and its local adaptation to various rice systems, as well as other crops (Systerm of Crop Intensification, or SCI), is quickly spreading among resource-limited farmers in Asia, Africa and Latin America, improving yields, food security, farm-level income, while protecting the environment.</p>

