	<div class="article"><h3>Cosplay</h3><div class="arrow"></div>
        
        <p>Before I discovered deviantART a little over a year and a half ago, I remember being rather disgusted by the idea of cosplay (short for "costume play"). What kind of people, adults especially, dressed up in ridiculous costumes, pretending to be some fictional character from some anime or cartoon when it wasn't even Halloween?</p>
        <p>I recall meeting a college student when I was a freshman in highschool, who I thought was very intelligent. It is interesting to look back and realize that the moment I found out that they enjoyed cosplaying, my entire view of them shifted. I couldn't understand why this bright and respectable college student was making a fool of themselves donning bright fairy costumes and trapaising through anime conventions like a child.</p>
        <p>It is not as though I suddenly also became an avid cosplay fan after seeing all of the photos on deviantART, but I have acquired an pretty great appreciation of them. Cosplay tends to have an unfavorable reputation among the general public, as most see such activities as strange and bizarre. In addition, much of cosplay is done by amateurs who tend not to have abundant skills or experience with photography. However, this is no reason to completely dismiss the entire genre of cosplay, as I had once done.</p>
        <p>Cosplay is just another form of fanart. Just as I have seen some brilliant fanart and some fascinating fanfiction, cosplay is just another medium for people to express their ideas and show their appreciation for another's work. I personally agree with most people in that original art has a bit more merit than fanart, but I still see fanart as a fascinating part of the art community. To take another's idea and put your own twist on it can often take a creative leap that is not restricted to only original art. </p>
        <p>Furthermore, cosplay isn't merely about dressing up as a character. Cosplay involves photography, modeling, costumes, and makeup, which are all legitimate forms of art. Cosplay artists often have a surprising number of skills at their disposal, which they can put together to create some of the most breathtaking artworks I have seen.</p>
